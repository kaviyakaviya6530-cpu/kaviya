class Switch {
    public static void main(String[] args) {
        int a=4;
        switch(a){
            case 1:
                System.out.println("pongal");
                break;
            case 2:
                System.out.println("diwali");
                break;
            case 3:
                System.out.println("new year");
                break;
            default:
                System.out.println("not a festivel");
                break;
        }
    }
}
